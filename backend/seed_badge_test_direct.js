function shuffleArray(array) {
  const arr = [...array];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

// Generate options for a question using other questions' answers as distractors
function generateMCQOptions(currentQ, allQAs) {
  const correctAnswer = currentQ.a;
  const category = classifyQuestion(currentQ.q);
  
  // Filter out distractors that have the same question text
  let validDistractors = allQAs
    .filter(item => {
      const isSameCategory = classifyQuestion(item.q) === category;
      return item.q !== currentQ.q && item.a !== correctAnswer && isSameCategory;
    })
    .map(item => item.a);
  
  validDistractors = [...new Set(validDistractors)].map(d => d.trim()).filter(Boolean);
  
  // If not enough in same category, pad from general pool
  if (validDistractors.length < 3) {
    let generalDistractors = allQAs
      .filter(item => item.q !== currentQ.q && item.a !== correctAnswer)
      .map(item => item.a);
    generalDistractors = [...new Set(generalDistractors)].map(d => d.trim()).filter(Boolean);
    for (const d of generalDistractors) {
      if (validDistractors.length >= 3) break;
      if (!validDistractors.includes(d)) {
        validDistractors.push(d);
      }
    }
  }
  
  while (validDistractors.length < 3) {
    validDistractors.push(`മറ്റ് ഉത്തരം ${validDistractors.length + 1}`);
  }
  
  const chosenDistractors = shuffleArray(validDistractors).slice(0, 3);
  const options = shuffleArray([correctAnswer, ...chosenDistractors]);
  const correctAnswerIndex = options.indexOf(correctAnswer);
  
  // Determine difficulty
  let difficulty = 'Medium';
  if (category === 'speed_limit' || category === 'number_plate') {
    difficulty = 'Easy';
  } else if (category === 'technical') {
    difficulty = 'Hard';
  }
  
  // Determine explanation
  let explanation = '';
  if (category === 'speed_limit') {
    explanation = `ഈ വാഹനത്തിന്റെ പരമാവധി അനുവദനീയമായ വേഗത മണിക്കൂറിൽ ${correctAnswer} ആണ്. റോഡ് സുരക്ഷയും വേഗപരിധിയും എപ്പോഴും പാലിക്കേണ്ടതുണ്ട്.`;
  } else if (category === 'color') {
    explanation = `വാഹനത്തിന്റെ തിരിച്ചറിയലിനായി മോട്ടോർ വാഹന വകുപ്പ് നിർദ്ദേശിച്ചിട്ടുള്ള നിറമാണിത്: ${correctAnswer}.`;
  } else if (category === 'duration') {
    explanation = `മോട്ടോർ വാഹന നിയമം അനുസരിച്ച് ഇതിന്റെ കാലാവധി അല്ലെങ്കിൽ ഗ്രേസ് പിരിയഡ് ${correctAnswer} ആയി നിജപ്പെടുത്തിയിരിക്കുന്നു.`;
  } else if (category === 'officer') {
    explanation = `ഈ കാര്യങ്ങൾ പരിശോധിക്കാനോ സർട്ടിഫിക്കറ്റ് നൽകാനോ ഉള്ള നിയമാനുസൃത അധികാരം ${correctAnswer} എന്ന പദവിയിലുള്ള ഉദ്യോഗസ്ഥനാണ് ഉള്ളത്.`;
  } else if (category === 'number_plate') {
    explanation = `വ്യത്യസ്ത ഇനം വാഹനങ്ങളെ തിരിച്ചറിയാൻ നമ്പർ പ്ലേറ്റുകൾക്ക് പ്രത്യേകം വർണ്ണ ക്രമീകരണമുണ്ട്: ${correctAnswer}.`;
  } else if (category === 'technical') {
    explanation = `വാഹനത്തിന്റെ സുഗമമായ സുരക്ഷാ പ്രവർത്തനങ്ങൾ നടത്തുന്നതിനായി ${correctAnswer} ശരിയായി പ്രവർത്തിക്കുന്നുണ്ടെന്ന് ഉറപ്പാക്കേണ്ടതുണ്ട്.`;
  } else if (category === 'first_aid_accident') {
    explanation = `വാഹനാപകടം ഉണ്ടാകുമ്പോൾ പ്രഥമശുശ്രൂഷ നൽകുന്നതിനും ജീവൻ രക്ഷിക്കുന്നതിനും ഈ പ്രവർത്തനം വളരെ അനിവാര്യമാണ്.`;
  } else {
    explanation = `മോട്ടോർ വാഹന ചട്ടപ്രകാരം ഡ്രൈവർമാർ പാലിക്കേണ്ട പ്രധാന റോഡ് നിയമങ്ങളിൽ ഒന്നാണിത്.`;
  }
  
  return {
    questionText: currentQ.q,
    options: options,
    correctAnswerIndex: correctAnswerIndex,
    category: category,
    difficulty: difficulty,
    explanation: explanation
  };
}

